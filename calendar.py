def main():
    # this program makes the calendar

    month = int(input("Enter your number of month(ex: 9 for September: "))

    # input
    if month == 1:
        print("\nThis is the month of January")

    elif month == 2:
        print("\nThis is the month of February")

    elif month == 3:
        print("\nThis is the month of March")

    elif month == 4:
        print("\nThis is the month of April")

    elif month == 5:
        print("\nThis is the month of May")

    elif month == 6:
        print("\nThis is the month of June")

    elif month == 7:
        print("\nThis is the month of July")

    elif month == 8:
        print("\nThis is the month of August.")

    elif month == 9:
        print("\nThis is the month of September.")

    elif month == 10:
        print("\nThis is the month of October.")

    elif month == 11:
        print("\nThis is the month of November.")

    elif month == 12:
        print("\nThis is the month of December.")

    print("\nDone")


if __name__ == "__main__":
    main()
